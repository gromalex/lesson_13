// Основные команды git
// $ git init - инициализация git репозитория
// $ git status - проверить состояние файлов

// $ git add . - проиндексировать текущее состояния/новые изменения, . - это все файлы с изменениями в репозитории
// $ git commit -m "Name commit" - коммит изменений

// $ git branch name_branch - создать ветку с именем name_branch
// $ git checkout name_branch - перейти в ветку name_branch
// $ git checkout -b name_branch - создать ветку name_branch и сразу перейти в неё
// $ git checkout name_file - отменить изменения в файле name_file

// Чтобы слить ветку name_branch с основной веткой master, надо:
// 1. Закомитить изменения в ветке name_branch
// 2. Перейти в ветку master $ git checkout master
// 3. Слить ветку name_branch с master $ git merge name_branch

